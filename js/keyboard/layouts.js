/* ============================================================
   keyboard/layouts.js — キーボード・レイアウト定義
   ------------------------------------------------------------
   各キー:
     { c: center,  l: left, r: right, u: up, d: down,
       fn: 機能キー('bs'|'space'|'ok'|'dakuten'|'mode-hira'|'mode-alpha'|'mode-num'|'caps'),
       label: 表示ラベル(fnキー用) }
   centerが設定されてればタップ文字、flickで l/r/u/d が出る。
   ============================================================ */

(function () {
    // --- ひらがな10キー ---
    // 4列 × 4行 (最終列は機能キー)
    const HIRAGANA = [
        [
            { c: 'あ', u: 'う', l: 'い', r: 'え', d: 'お' },
            { c: 'か', u: 'く', l: 'き', r: 'け', d: 'こ' },
            { c: 'さ', u: 'す', l: 'し', r: 'せ', d: 'そ' },
            { fn: 'bs', label: '⌫' },
        ],
        [
            { c: 'た', u: 'つ', l: 'ち', r: 'て', d: 'と' },
            { c: 'な', u: 'ぬ', l: 'に', r: 'ね', d: 'の' },
            { c: 'は', u: 'ふ', l: 'ひ', r: 'へ', d: 'ほ' },
            { fn: 'space', label: '␣' },
        ],
        [
            { c: 'ま', u: 'む', l: 'み', r: 'め', d: 'も' },
            { c: 'や', u: 'ゆ', l: '(', r: ')', d: 'よ' },
            { c: 'ら', u: 'る', l: 'り', r: 'れ', d: 'ろ' },
            { fn: 'dakuten', label: '゛゜小' },
        ],
        [
            { fn: 'mode-alpha', label: 'ABC' },
            { c: 'わ', u: 'ん', l: 'を', r: 'ー', d: '、' },
            { fn: 'mode-num', label: '123' },
            { fn: 'ok', label: 'OK' },
        ],
    ];

    // --- 英字 (フリック式) ---
    const ALPHA = [
        [
            { c: '@', u: '#', l: '/', r: '&', d: '_' },
            { c: 'a', u: 'c', l: 'b' },
            { c: 'd', u: 'f', l: 'e' },
            { fn: 'bs', label: '⌫' },
        ],
        [
            { c: 'g', u: 'i', l: 'h' },
            { c: 'j', u: 'l', l: 'k' },
            { c: 'm', u: 'o', l: 'n' },
            { fn: 'space', label: '␣' },
        ],
        [
            { c: 'p', u: 'r', l: 'q', r: 's' },
            { c: 't', u: 'v', l: 'u' },
            { c: 'w', u: 'y', l: 'x', r: 'z' },
            { fn: 'caps', label: 'a/A' },
        ],
        [
            { fn: 'mode-hira', label: 'あ' },
            { c: "'", u: '!', l: '.', r: ',', d: '?' },
            { fn: 'mode-num', label: '123' },
            { fn: 'ok', label: 'OK' },
        ],
    ];

    // --- 数字テンキー ---
    const NUMBER = [
        [
            { c: '1' },
            { c: '2' },
            { c: '3' },
            { fn: 'bs', label: '⌫' },
        ],
        [
            { c: '4' },
            { c: '5' },
            { c: '6' },
            { c: '.' },
        ],
        [
            { c: '7' },
            { c: '8' },
            { c: '9' },
            { c: '-' },
        ],
        [
            { fn: 'mode-hira', label: 'あ' },
            { c: '0' },
            { fn: 'mode-alpha', label: 'ABC' },
            { fn: 'ok', label: 'OK' },
        ],
    ];

    // --- 濁点・半濁点・小文字 変換テーブル ---
    // 1タップごとにサイクル: 元 → 濁点 → 半濁点 → 小文字 → 元 …
    // 有効な変換がない段階はスキップ
    const CYCLE = {
        // 基本母音: 小文字化のみ
        'あ': ['ぁ'], 'い': ['ぃ'], 'う': ['ぅ', 'ゔ'], 'え': ['ぇ'], 'お': ['ぉ'],
        // カ行
        'か': ['が'], 'き': ['ぎ'], 'く': ['ぐ'], 'け': ['げ'], 'こ': ['ご'],
        // サ行
        'さ': ['ざ'], 'し': ['じ'], 'す': ['ず'], 'せ': ['ぜ'], 'そ': ['ぞ'],
        // タ行
        'た': ['だ'], 'ち': ['ぢ'], 'つ': ['っ', 'づ'], 'て': ['で'], 'と': ['ど'],
        // ハ行 (濁点 → 半濁点)
        'は': ['ば', 'ぱ'], 'ひ': ['び', 'ぴ'], 'ふ': ['ぶ', 'ぷ'], 'へ': ['べ', 'ぺ'], 'ほ': ['ぼ', 'ぽ'],
        // ヤ行: 小文字
        'や': ['ゃ'], 'ゆ': ['ゅ'], 'よ': ['ょ'],
        // ワ行
        'わ': ['ゎ'],
        // カタカナ版 (キーボードのkatakanaモードで使用)
        'ア': ['ァ'], 'イ': ['ィ'], 'ウ': ['ゥ', 'ヴ'], 'エ': ['ェ'], 'オ': ['ォ'],
        'カ': ['ガ'], 'キ': ['ギ'], 'ク': ['グ'], 'ケ': ['ゲ'], 'コ': ['ゴ'],
        'サ': ['ザ'], 'シ': ['ジ'], 'ス': ['ズ'], 'セ': ['ゼ'], 'ソ': ['ゾ'],
        'タ': ['ダ'], 'チ': ['ヂ'], 'ツ': ['ッ', 'ヅ'], 'テ': ['デ'], 'ト': ['ド'],
        'ハ': ['バ', 'パ'], 'ヒ': ['ビ', 'ピ'], 'フ': ['ブ', 'プ'], 'ヘ': ['ベ', 'ペ'], 'ホ': ['ボ', 'ポ'],
        'ヤ': ['ャ'], 'ユ': ['ュ'], 'ヨ': ['ョ'],
        'ワ': ['ヮ'],
    };

    // 濁点などサイクル中の逆引き (前段階から次段階へ遷移する辞書ではなく、末端かどうかを判定)
    // cycleNext('は') → 'ば',  cycleNext('ば') → 'ぱ',  cycleNext('ぱ') → 'は'
    const ALL_CYCLE_CHAIN = {};
    Object.keys(CYCLE).forEach((base) => {
        const chain = [base, ...CYCLE[base]];
        for (let i = 0; i < chain.length; i++) {
            ALL_CYCLE_CHAIN[chain[i]] = chain[(i + 1) % chain.length];
        }
    });

    function cycleNext(ch) {
        return ALL_CYCLE_CHAIN[ch] || null;
    }

    // --- ひらがな ⇄ カタカナ変換 ---
    function hiraToKata(s) {
        return s.replace(/[\u3041-\u3096]/g, (ch) => String.fromCharCode(ch.charCodeAt(0) + 0x60));
    }
    function kataToHira(s) {
        return s.replace(/[\u30A1-\u30F6]/g, (ch) => String.fromCharCode(ch.charCodeAt(0) - 0x60));
    }

    window.KeyboardLayouts = {
        HIRAGANA,
        ALPHA,
        NUMBER,
        cycleNext,
        hiraToKata,
        kataToHira,
    };
})();
